//【数値の演算】
//問題6：四則演算まとめ
//• int型で2つの数字を作る
//• 足し算・引き算・掛け算・割り算をそれぞれprintlnで表示する

class A0206 {
    public static void main(String[] args) {

//        Variable Definition
        int a = 13;
        int b = 12;

//        Output
        System.out.println(a + b);
        System.out.println(a - b);
        System.out.println(a * b);
        System.out.println(a / b);
    }
}
